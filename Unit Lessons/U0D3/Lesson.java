public class Lesson {
    public static void main(String[] args) {
        // variables
        int number = 5; // assignment operator

        // single = assigns a value
        // different from ==, this compares a value

        // different assignment operators
        number += 6; // number = number + 6
        number -= 3; // number = number - 3
        number *= 10; // number = number * 10
        number /= 2; // number = number / 2

        // Bonus: Bitwise operators &= ||=

        // increment and decrement operators
        // increment: ++
        number++; // equals number + 1

        // decrement: --
        number--; // equals number - 1

        // boolean operators, eval to True or False (use in if statements)
        // CANNOT USE FOR OBJECTS (Ex: String)

        /*
         * number == 5; //equals number != 10; //not equal to number <= 15; // less than or equal to
         * number >= 20; // greater than or equal to
         */
    }
}
